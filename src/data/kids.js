    export const getTopicWords = (topicId, level = 'easy') => {
      const topicData = kidsVocabulary[topicId];
      if (!topicData) return kidsVocabulary['vocabulary-colors'].words; 

      if (topicData.levels && topicData.levels[level]) {
        return topicData.levels[level].words.map(word => ({
          ...word,
          en: topicId === 'vocabulary-numbers' ? numberToWord(word.num).toUpperCase() : word.en
        }));
      }
      return topicData.words || kidsVocabulary['vocabulary-colors'].words;
    };

    export const numberToWord = (num) => {
        const a = ['','ONE','TWO','THREE','FOUR', 'FIVE','SIX','SEVEN','EIGHT','NINE','TEN','ELEVEN','TWELVE','THIRTEEN','FOURTEEN','FIFTEEN','SIXTEEN','SEVENTEEN','EIGHTEEN','NINETEEN'];
        const b = ['', '', 'TWENTY','THIRTY','FORTY','FIFTY', 'SIXTY','SEVENTY','EIGHTY','NINETY'];
        
        if (num === 0) return 'ZERO';
        if (num < 20) return a[num];
        if (num < 100) return b[Math.floor(num/10)] + (num % 10 !== 0 ? '-' + a[num % 10] : '');
        if (num < 1000) {
            if (num % 100 === 0) return a[Math.floor(num/100)] + ' HUNDRED';
            return a[Math.floor(num/100)] + ' HUNDRED AND ' + numberToWord(num % 100);
        }
        if (num === 1000) return 'ONE THOUSAND';
        return num.toString(); 
    };

    export const getNumberWordsByLevel = (level) => {
        const numbersData = kidsVocabulary['vocabulary-numbers'].levels[level]?.words || [];
        return numbersData.map(w => ({...w, en: numberToWord(w.num).toUpperCase()}));
    };
  